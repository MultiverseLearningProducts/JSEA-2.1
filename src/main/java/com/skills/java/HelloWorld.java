package com.skills.java;

/**
 * A simple Hello World program that demonstrates basic Java program structure.
 * This program serves as an introduction to Java programming fundamentals.
 */
public class HelloWorld {
    
    /**
     * The main method - the entry point of the Java program.
     * This method is called by the JVM when the program starts.
     */
    public static void main(String[] args) {
        
        // This program prints a simple greeting message to the console
        
        // Print "Hello, World!" to the console
        System.out.println("Hello, World!");
        
        /* 
         * The main method is essential because:
         * 1. It's the entry point that the JVM looks for
         * 2. It must be public so the JVM can access it
         * 3. It must be static so it can run without creating an object
         * 4. It must be named exactly "main"
         */
    }
} 